    private void jButton11ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton11ActionPerformed
        lblDatos.setVisible(true);
        this.ache.setRowCount(0);
        this.getjTable1().setModel(ache);

        /*el swingworker sirve para ejecutar tareas alternas en el hilo principal del gui
        más que nada se usa pal efecto del label "cargando datos..."*/
        new SwingWorker<Void, Void>() {
            @Override
            public Void doInBackground() {
                try {
                    /*llenar tabla pacientes con los datos de pacientes asignados al doctor*/
                    String jsonListaPacientes = RestManager.getInstance().obtenerListaPacientesAsignados();
                    listaPacientesJava = objectMapper.readValue(jsonListaPacientes, ListaPacientes.class);
                    for (int i = 0; i < listaPacientesJava.getListaPacientesJava().size(); i++) {
                        /*popular la tabla, con su respectivo formato de fecha*/
                        Date date = convertirJsonFecha.parse(listaPacientesJava.getListaPacientesJava().get(i).getFechaNac());
                        String nombreCompleto = listaPacientesJava.getListaPacientesJava().get(i).getNombres() + " " + listaPacientesJava.getListaPacientesJava().get(i).getApellidos();
                        panelPacientePrincipal.this.ache.addRow(new Object[]{
                            nombreCompleto,
                            aplicarFormatoFecha.format(date),
                            listaPacientesJava.getListaPacientesJava().get(i).getNumTelefono()
                        });
                        /*ir añadiendo las filas*/
                        panelPacientePrincipal.this.getjTable1().setModel(ache);
                    }
                } catch (IOException ex) {
                    System.out.println(ex.getMessage() + " add " + ex.getCause());
                } catch (ParseException ex) {
                    System.out.println(ex.getMessage() + " bdd " + ex.getCause());
                }

                if (fondoPrincipal.isVisible()) {
                    fondoPrincipal.setVisible(false);
                }
                if (!jPanel1.isVisible()) {
                    jPanel1.setVisible(true);
                }
                lblDatos.setVisible(false);
                return null;
            }

            @Override
            public void done() {
                lblDatos.setVisible(false);
                jButton2.setVisible(false); //hacer botones invisibles al clickear el fondo blanco
                jButton3.setVisible(false);
                jButton4.setVisible(false);
                jButton5.setVisible(false);
                jButton6.setVisible(false);
            }
        }.execute();


    }//GEN-LAST:event_jButton11ActionPerformed

