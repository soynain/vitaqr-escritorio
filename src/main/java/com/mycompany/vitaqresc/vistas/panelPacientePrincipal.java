    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int pacienteSeleccionado = this.getjTable1().getSelectedRow();
        ModificacionDatosPersonales modp = new ModificacionDatosPersonales();

        modp.indicePaciente = Integer.valueOf(listaPacientesJava.getListaPacientesJava().get(pacienteSeleccionado).getIdPrim());

        modp.txtNombre.setText(listaPacientesJava
                .getListaPacientesJava()
                .get(pacienteSeleccionado)
                .getNombres());

        modp.txtApellidos.setText(listaPacientesJava
                .getListaPacientesJava()
                .get(pacienteSeleccionado)
                .getApellidos());

        modp.txtTelefono.setText(listaPacientesJava
                .getListaPacientesJava()
                .get(pacienteSeleccionado)
                .getNumTelefono()
                .toString());

        modp.txtAltura.setText(listaPacientesJava
                .getListaPacientesJava()
                .get(pacienteSeleccionado)
                .getAltura());

        modp.txtPeso.setText(listaPacientesJava
                .getListaPacientesJava()
                .get(pacienteSeleccionado)
                .getPeso()
                .toString());

        try {
            Date fechaParseada = convertirJsonFecha.parse(listaPacientesJava
                    .getListaPacientesJava()
                    .get(pacienteSeleccionado)
                    .getFechaNac());
            Date nuevaFecha = aplicarFormatoFecha.parse(aplicarFormatoFecha.format(fechaParseada));

            modp.datoFecha.setDate(nuevaFecha);
        } catch (ParseException ex) {
            Logger.getLogger(panelPacientePrincipal.class.getName()).log(Level.SEVERE, null, ex);
        }

        if (listaPacientesJava
                .getListaPacientesJava()
                .get(pacienteSeleccionado)
                .getSexo().equals("Masculino")) {
            modp.cbSexo.setSelectedIndex(0);
        } else {
            modp.cbSexo.setSelectedIndex(1);
        }

        if (listaPacientesJava.getListaPacientesJava().get(pacienteSeleccionado).getDonanteorg().equals("Si")) {
            modp.cbDonante.setSelectedIndex(0);
        } else {
            modp.cbDonante.setSelectedIndex(1);
        }

        modp.setVisible(true);
    }//GEN-LAST:event_jButton3ActionPerformed
